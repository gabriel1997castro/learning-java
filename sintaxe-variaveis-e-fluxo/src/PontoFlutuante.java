
public class PontoFlutuante {
	public static void main(String[] args) {
		double salario = 1250.70;
		System.out.println("meu salario é " + salario);
		
		int divisao = 5 / 2;
		System.out.println(divisao);
		
		double divisaoComDouble = 5 / 2;
		System.out.println(divisaoComDouble);
		
		double divisaoComDoubleDeNovo = 5.0 / 2;
		System.out.println(divisaoComDoubleDeNovo);
	}
}
